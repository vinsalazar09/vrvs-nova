// VRVS Nova — Service Worker mínimo — Etapa 1
// Escopo: /public/ (quando servido a partir da raiz do projeto)
// Para escopo raiz, mover este arquivo para / ou configurar Service-Worker-Allowed.

const CACHE = 'vrvs-shell-v1';

const PRECACHE = [
  './icons/icon-192.png',
  './icons/icon-512.png'
];

self.addEventListener('install', e => {
  e.waitUntil(
    caches.open(CACHE).then(cache => cache.addAll(PRECACHE))
  );
  self.skipWaiting();
});

self.addEventListener('activate', e => {
  e.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.filter(k => k !== CACHE).map(k => caches.delete(k))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener('fetch', e => {
  e.respondWith(
    caches.match(e.request).then(cached => cached || fetch(e.request))
  );
});
